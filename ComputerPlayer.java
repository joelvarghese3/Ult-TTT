public class ComputerPlayer extends APlayer{


    public ComputerPlayer(String name, char playerLetter) {
        super(name, playerLetter);
    }


    @Override
    public String getName() {
        return super.getName();
    }

    @Override
    public void setName(String name) {
        super.setName(name);
    }


    @Override
    public char getPlayerLetter() {
        return super.getPlayerLetter();
    }




}
